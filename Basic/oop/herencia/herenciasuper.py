    def __init__(self, nombre, edad, lugarResidencia):

        self.nombre = nombre
        self.edad = edad
        self.lugarResidencia  = lugarResidencia

    def descripcion(self):
        print(f"Nombre {self.nombre} , edad: {edad} y Lugar de resedencia: {self.lugarResidencia}")

